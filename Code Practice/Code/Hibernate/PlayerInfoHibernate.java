public class Player {
	
		// Declaration of all the variables declared
	
    private int playerID;  
		private String playerFN;
		private String playerLN;
		private String playerUN;
		private String playerEmail;
		private String playerPhone;
  
public int getPlayerID() {  
    return playerID;  
}
  
public void setPlayerID(int playerID) {  
    this.playerID = playerID;  
}  

public String getPlayerFN() {  
    return playerFN;  
}  

public void setPlayerFN(String playerFN) {  
    this.playerFN = playerFN;  
}  

public String getPlayerLN() {  
    return playerLN;  
}  

public void setPlayerLN(String playerLN) {  
    this.playerLN = playerLN;  
}  

public String getPlayerUN() {  
    return playerUN;  
}  

public void setPlayerUN(String playerUN) {  
    this.playerUN = playerUN;  
}  

public String getPlayerEmail() {  
    return playerEmail;  
}  

public void setPlayerEmail(String playerEmail) {  
    this.playerEmail = playerEmail;  
} 

public String getPlayerPhone() {  
    return playerPhone;  
}  

public void setPlayerPhone(String playerPhone) {  
    this.playerPhone = playerPhone;  
} 

}
